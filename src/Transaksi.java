    private String hargaMenu() {
        String selected = (String) jComboBoxMenu.getSelectedItem();
        
        String SQL = "SELECT `harga` FROM `menu` WHERE nama='"+selected+"'";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        String harga = "";
        try {
            while(rs.next())
                harga = rs.getString(1);
        } catch (SQLException ex) {
            Logger.getLogger(Transaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
        jLabelHarga.setText("Rp "+harga);
        return harga;
    }
