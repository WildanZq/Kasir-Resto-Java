    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String menu = jTextMenu.getText();
        String harga = jTextHarga.getText();
        
        if("".equals(menu) || "".equals(harga)) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                int iharga = Integer.parseInt(harga);
            } catch(Exception e) {
                JOptionPane.showMessageDialog(this, "Harga harus angka", "Error", JOptionPane.WARNING_MESSAGE);
                jTextHarga.setText("");
                return;
            }
            
            String SQL = "INSERT INTO `menu`(`nama`,`harga`) VALUES('"+menu+"',"+harga+")";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Menu berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                getMenu();
                jTextMenu.setText("");
                jTextHarga.setText("");
            } else {
                JOptionPane.showMessageDialog(this,"Menu gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);   
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

