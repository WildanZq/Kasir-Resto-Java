    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String pembeli = jTextFieldPembeli.getText();
        String bayar = jTextFieldBayar.getText();
        String pesanan = (String) jComboBoxMenu.getSelectedItem();
        int harga = Integer.parseInt(hargaMenu());
        
        if("".equals(pembeli) || "".equals(jTextFieldBayar.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                int ibayar = Integer.parseInt(bayar);
            } catch(Exception e) {
                JOptionPane.showMessageDialog(this, "Bayar harus angka", "Error", JOptionPane.WARNING_MESSAGE);
                jTextHarga.setText("");
                return;
            }
            int ibayar = Integer.parseInt(bayar);
            int kembali = 0;
            if(ibayar > harga) {
                kembali = ibayar-harga;
            } else if(harga > ibayar) {
                JOptionPane.showMessageDialog(this, "Uang tidak cukup", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date tanggal = new Date();
            
            String SQL = "INSERT INTO `transaksi`(`tanggal`,`pembeli`,`pesanan`,`bayar`,`harga`,`kembali`) VALUES('"+dateFormat.format(tanggal)+"','"+pembeli+"','"+pesanan+"',"+bayar+","+harga+","+kembali+")";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Transaksi berhasil\nKembali: Rp "+kembali, "Sukses", JOptionPane.INFORMATION_MESSAGE);
                jTextFieldBayar.setText("");
                jTextFieldPembeli.setText("");
            } else {
                JOptionPane.showMessageDialog(this,"Transaksi gagal", "Gagal", JOptionPane.WARNING_MESSAGE);   
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void getMenu() {
        String SQL = "SELECT * FROM `menu`";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            jComboBoxMenu.removeAllItems();
            while(rs.next()) {
                String nama = rs.getString(2);
                jComboBoxMenu.addItem(nama);
            }
        } catch (SQLException ex){
            Logger.getLogger(Transaksi.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
